def print_string(string, size, color, x, y):
    clear()
    stamp()
    origin_x = pos()[0]
    origin_y = pos()[1]
    ht()
    penup()
    goto(x, y)
    pendown()
    pencolor(color)
    write(string, font=('04b_21', size, "normal"))
    penup()
    goto(origin_x, origin_y)
    st()

